#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dup;
	size_t	size;
	size_t	i;

	i = 0;
	size = ft_strlen(src);
	dup = (char *)malloc(sizeof(char) * (size + 1));
	if (!dup)
		return (NULL);
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
